# Operadores relacionales: diseñados para comparar un par de datos numéricos

"""
    >
    <
    >=
    <=
    ==
    !=
"""

numero_uno = 10
numero_dos = 20

resulto = numero_uno > numero_dos
print(resulto)

resulto = numero_uno < numero_dos
print(resulto)

resulto = numero_uno >= numero_dos
print(resulto)

numero_uno = 20
numero_dos = 20

resulto = numero_uno <= numero_dos
print(resulto)

resulto = numero_uno == numero_dos
print(resulto)

resulto = numero_uno != numero_dos
print(resulto)
